ServerEvents.recipes(event => {
  function filling(output, waterInput, canteenInput) {
event.shapeless(output.strongNBT(), [

      canteenInput.strongNBT(),
      waterInput.strongNBT(),
      waterInput.strongNBT(),
      waterInput.strongNBT(),
      waterInput.strongNBT(),
      waterInput.strongNBT(),
      waterInput.strongNBT(),
      waterInput.strongNBT(),
      waterInput.strongNBT()

    ]).replaceIngredient('minecraft:potion', 'minecraft:glass_bottle')
}  

filling(
  // output
  Item.of('thirstcanteen:leather_canteen_full', '{Damage:0,Purity:2}'),
  // waterInput
  Item.of('minecraft:potion', '{Potion:"minecraft:water",Purity:2}'),
  // canteenInput
  Item.of('thirstcanteen:leather_canteen_full', '{Damage:1,Purity:2}')
)

})

